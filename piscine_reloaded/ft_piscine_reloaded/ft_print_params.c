void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char *argv[])
{
	int i;

	i = 1;
	while (argv[i])
	{
		ft_putstr(argv[i]);
		ft_putstr("\n");
		argc++;
		i++;
	}
	return (0);
}
